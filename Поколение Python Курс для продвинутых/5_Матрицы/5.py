n = int(input())
m = [[int(i) for i in input().split()] for _ in range(n)]
maximum = m[0][0]
for i in range(n):
    for j in range(n):
        if i+j+1 <= n and i >= j and m[i][j] > maximum:
            maximum = m[i][j]
        if i+j+1 >= n and i <= j and m[i][j] > maximum:
            maximum = m[i][j]
print(maximum)

