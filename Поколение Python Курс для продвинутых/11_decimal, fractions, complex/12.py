from fractions import Fraction as F
from math import gcd

n = int(input())
res = set()

while n != 1:
    a = n - 1
    for i in range(a, 0, -1):
        if gcd(a, n) == 1 and 0 < F(i, n) < 1:
            res.add(F(i, n))
    n -= 1
print(*sorted(res), sep='\n')
