from functools import reduce
from operator import mul


def product_of_odds(data):   # data - —Å–ø–∏—Å–æ–∫ —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª
    from functools import reduce
    data_filter = filter(lambda x: x % 2 == 1, data)
    result = reduce(lambda x, y: x*y, data_filter, 1)
    return result


