    tuples = (24 * 60, ('–¥–µ–Ω—å', '–¥–Ω—è', '–¥–Ω–µ–π')), (60, ('—á–∞—Å', '—á–∞—Å–∞', '—á–∞—Å–æ–≤')), (1, ('–º–∏–Ω—É—Ç–∞', '–º–∏–Ω—É—Ç—ã', '–º–∏–Ω—É—Ç'))
