import itertools as it


def roundrobin(*args):
    yield from (i for elem in it.zip_longest(*args, fillvalue='-') for i in elem if i != '-')


numbers = iter([1, 2, 3])
nones = iter([None, None, None, None])

print(*roundrobin(numbers, nones))