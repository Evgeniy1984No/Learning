import csv


def Cheap_product(d: dict) -> dict:
    temp = [list(d.items())[0]]
    products = list(sorted(list(d.items())[1:], key=lambda x: (int(x[1]), x[0])))
    temp += [products[0]]
    return dict(temp)


with open('C:/Users/kandz/Downloads/prices.csv', encoding='utf-8') as f:
    rows = list(csv.DictReader(f, delimiter=';'))
    cheaper_temp = [Cheap_product(d) for d in rows]
    cheaper = min(cheaper_temp, key=lambda x: (int(list(x.items())[1][1]), list(x.items())[1][0], list(x.items())[0][1]))
    print(f'{list(cheaper.keys())[1]}: {list(cheaper.values())[0]}')
