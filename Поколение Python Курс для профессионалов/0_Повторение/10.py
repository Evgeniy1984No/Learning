def get_biggest(numbers: list) -> int:
    if numbers:
        lst_sort = (sorted(numbers, key=lambda x: str(x) * max(numbers, key=lambda y: len(str(y))), reverse=True))
        return int(''.join(map(str, lst_sort)))
    return -1


